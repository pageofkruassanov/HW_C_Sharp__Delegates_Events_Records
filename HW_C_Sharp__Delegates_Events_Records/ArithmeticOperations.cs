    public static class ArithmeticOperations
    {
        static public double Addition(double a, double b)
        {
            return (a + b);
        }

        static public double Subtraction(double a, double b)
        {
            return (a - b);
        }

        static public double Multiplication(double a, double b)
        {
            return (a * b);
        }
    }
